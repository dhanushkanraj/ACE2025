-- Create the database with backticks to handle spaces
CREATE DATABASE Freight_Forwarding_System;

-- Switch to the newly created database
USE Freight_Forwarding_System;

-- Create the tables

CREATE TABLE Client (
    Client_ID INT PRIMARY KEY,
    Client_Name VARCHAR(100),
    Contact_Info VARCHAR(100),
    Address VARCHAR(200)
);

CREATE TABLE Shipment (
    Shipment_ID INT PRIMARY KEY,
    Origin VARCHAR(100),
    Destination VARCHAR(100),
    Shipping_Date DATE,
    Arrival_Date DATE,
    Status VARCHAR(50),
    Weight DECIMAL(10, 2),
    Volume DECIMAL(10, 2),
    Client_ID INT,
    FOREIGN KEY (Client_ID) REFERENCES Client(Client_ID)
);

CREATE TABLE Container (
    Container_ID INT PRIMARY KEY,
    Container_Type VARCHAR(50),
    Container_Size VARCHAR(50),
    Weight_Capacity DECIMAL(10, 2),
    Container_Status VARCHAR(50),
    Arrival_Date DATE,
    Departure_Date DATE,
    Shipment_ID INT,
    FOREIGN KEY (Shipment_ID) REFERENCES Shipment(Shipment_ID)
);

CREATE TABLE Carrier (
    Carrier_ID INT PRIMARY KEY,
    Carrier_Name VARCHAR(100),
    Contact_Info VARCHAR(100),
    Address VARCHAR(200),
    Carrier_Type VARCHAR(50)
);



CREATE TABLE Booking (
    Booking_ID INT PRIMARY KEY,
    Shipment_ID INT,
    Container_ID INT,
    Carrier_ID INT,
    Booking_Date DATE,
    Departure_Date DATE,
    Arrival_Date DATE,
    Booking_Status VARCHAR(50),
    FOREIGN KEY (Shipment_ID) REFERENCES Shipment(Shipment_ID),
    FOREIGN KEY (Container_ID) REFERENCES Container(Container_ID),
    FOREIGN KEY (Carrier_ID) REFERENCES Carrier(Carrier_ID)
);
CREATE TABLE Route (
    Route_ID INT PRIMARY KEY,
    Origin VARCHAR(100),
    Destination VARCHAR(100),
    Distance DECIMAL(10, 2),
    Estimated_Duration TIME
);
CREATE TABLE Invoice (
    Invoice_ID INT PRIMARY KEY,
    Shipment_ID INT,
    Client_ID INT,
    Amount DECIMAL(10, 2),
    Due_Date DATE,
    Invoice_Status VARCHAR(50),
    FOREIGN KEY (Shipment_ID) REFERENCES Shipment(Shipment_ID),
    FOREIGN KEY (Client_ID) REFERENCES Client(Client_ID)
);

CREATE TABLE Tracking (
    Tracking_ID INT PRIMARY KEY,
    Shipment_ID INT,
    Location VARCHAR(100),
    Date_Time DATETIME,
    Tracking_Status VARCHAR(50),
    FOREIGN KEY (Shipment_ID) REFERENCES Shipment(Shipment_ID)
);

CREATE TABLE Customs (
    Customs_ID INT PRIMARY KEY,
    Shipment_ID INT,
    Declaration_Date DATE,
    Clearance_Status VARCHAR(50),
    Duty_Amount DECIMAL(10, 2),
    FOREIGN KEY (Shipment_ID) REFERENCES Shipment(Shipment_ID)
);

CREATE TABLE Document (
    Document_ID INT PRIMARY KEY,
    Shipment_ID INT,
    Document_Type VARCHAR(50),
    Description TEXT,
    Document_Status VARCHAR(50),
    FOREIGN KEY (Shipment_ID) REFERENCES Shipment(Shipment_ID)
);
